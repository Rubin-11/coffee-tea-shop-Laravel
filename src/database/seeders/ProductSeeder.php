/**
 * Seeder –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è —Ç–æ–≤–∞—Ä–æ–≤ —Å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è–º–∏ –∏ —Ç–µ–≥–∞–º–∏
 * 
 * –°–æ–∑–¥–∞–µ—Ç —Ä–µ–∞–ª–∏—Å—Ç–∏—á–Ω—ã–π –∞—Å—Å–æ—Ä—Ç–∏–º–µ–Ω—Ç –º–∞–≥–∞–∑–∏–Ω–∞:
 * - 30-35 —Ç–æ–≤–∞—Ä–æ–≤ –∫–æ—Ñ–µ (–¥–ª—è —Ä–∞–∑–ª–∏—á–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π –∏ –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–π)
 * - 8-10 —Ç–æ–≤–∞—Ä–æ–≤ —á–∞—è
 * - 5-7 –∞–∫—Å–µ—Å—Å—É–∞—Ä–æ–≤
 * 
 * –ö –∫–∞–∂–¥–æ–º—É —Ç–æ–≤–∞—Ä—É –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è:
 * - 2-4 –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è (–æ–¥–Ω–æ –≥–ª–∞–≤–Ω–æ–µ)
 * - 1-3 —Ç–µ–≥–∞ ("–ù–æ–≤–∏–Ω–∫–∞", "–•–∏—Ç –ø—Ä–æ–¥–∞–∂", "–ê–∫—Ü–∏—è" –∏ —Ç.–¥.)
 * - –ü—Ä–∏–≤—è–∑–∫–∞ –∫ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
 */
class ProductSeeder extends Seeder
{
    use WithoutModelEvents;

    /**
     * –ú–∞—Å—Å–∏–≤ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–æ–∑–¥–∞–Ω–Ω—ã—Ö —Ç–µ–≥–æ–≤ (–∫–µ—à)
     * 
     * @var \Illuminate\Support\Collection
     */
    private $tags;

    /**
     * –ú–∞—Å—Å–∏–≤ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–æ–∑–¥–∞–Ω–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π (–∫–µ—à)
     * 
     * @var \Illuminate\Support\Collection
     */
    private $categories;

    /**
     * –ó–∞–ø—É—Å–∫ seeder'–∞ –¥–ª—è –∑–∞–ø–æ–ª–Ω–µ–Ω–∏—è —Ç–æ–≤–∞—Ä–æ–≤
     * 
     * –°–æ–∑–¥–∞–µ—Ç —Ç–æ–≤–∞—Ä—ã –≤ —Å–ª–µ–¥—É—é—â–µ–º –ø–æ—Ä—è–¥–∫–µ:
     * 1. –¢–æ–≤–∞—Ä—ã –∫–æ—Ñ–µ (30-35 —à—Ç)
     * 2. –¢–æ–≤–∞—Ä—ã —á–∞—è (8-10 —à—Ç)
     * 3. –ê–∫—Å–µ—Å—Å—É–∞—Ä—ã (5-7 —à—Ç)
     * 
     * –î–ª—è –∫–∞–∂–¥–æ–≥–æ —Ç–æ–≤–∞—Ä–∞ —Å–æ–∑–¥–∞—é—Ç—Å—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏ –ø—Ä–∏–≤—è–∑—ã–≤–∞—é—Ç—Å—è —Ç–µ–≥–∏.
     */
    public function run(): void
    {
        $this->command->info('‚òï –°–æ–∑–¥–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–æ–≤ –º–∞–≥–∞–∑–∏–Ω–∞...');
        $this->command->newLine();

        // –ó–∞–≥—Ä—É–∂–∞–µ–º –≤—Å–µ —Ç–µ–≥–∏ –∏ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –≤ –ø–∞–º—è—Ç—å –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –¥–æ—Å—Ç—É–ø–∞
        $this->tags = Tag::all();
        $this->categories = Category::all()->keyBy('slug');

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –µ—Å—Ç—å –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∏ —Ç–µ–≥–∏
        if ($this->categories->isEmpty()) {
            $this->command->error('‚ùå –û—à–∏–±–∫–∞: —Å–Ω–∞—á–∞–ª–∞ –Ω—É–∂–Ω–æ –∑–∞–ø—É—Å—Ç–∏—Ç—å CategorySeeder!');
            return;
        }

        if ($this->tags->isEmpty()) {
            $this->command->error('‚ùå –û—à–∏–±–∫–∞: —Å–Ω–∞—á–∞–ª–∞ –Ω—É–∂–Ω–æ –∑–∞–ø—É—Å—Ç–∏—Ç—å TagSeeder!');
            return;
        }

        // –°—á–µ—Ç—á–∏–∫ —Å–æ–∑–¥–∞–Ω–Ω—ã—Ö —Ç–æ–≤–∞—Ä–æ–≤
        $totalProducts = 0;

        // 1. –°–æ–∑–¥–∞–µ–º —Ç–æ–≤–∞—Ä—ã –∫–æ—Ñ–µ (30-35 —à—Ç)
        $this->command->info('1Ô∏è‚É£  –°–æ–∑–¥–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–æ–≤ –∫–æ—Ñ–µ...');
        $coffeeProducts = $this->createCoffeeProducts();
        $totalProducts += $coffeeProducts;
        $this->command->info("   ‚úÖ –°–æ–∑–¥–∞–Ω–æ —Ç–æ–≤–∞—Ä–æ–≤ –∫–æ—Ñ–µ: {$coffeeProducts}");
        $this->command->newLine();

        // 2. –°–æ–∑–¥–∞–µ–º —Ç–æ–≤–∞—Ä—ã —á–∞—è (8-10 —à—Ç)
        $this->command->info('2Ô∏è‚É£  –°–æ–∑–¥–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–æ–≤ —á–∞—è...');
        $teaProducts = $this->createTeaProducts();
        $totalProducts += $teaProducts;
        $this->command->info("   ‚úÖ –°–æ–∑–¥–∞–Ω–æ —Ç–æ–≤–∞—Ä–æ–≤ —á–∞—è: {$teaProducts}");
        $this->command->newLine();

        // 3. –°–æ–∑–¥–∞–µ–º –∞–∫—Å–µ—Å—Å—É–∞—Ä—ã (5-7 —à—Ç)
        $this->command->info('3Ô∏è‚É£  –°–æ–∑–¥–∞–Ω–∏–µ –∞–∫—Å–µ—Å—Å—É–∞—Ä–æ–≤...');
        $accessoryProducts = $this->createAccessoryProducts();
        $totalProducts += $accessoryProducts;
        $this->command->info("   ‚úÖ –°–æ–∑–¥–∞–Ω–æ –∞–∫—Å–µ—Å—Å—É–∞—Ä–æ–≤: {$accessoryProducts}");
        $this->command->newLine();

        // –ò—Ç–æ–≥–æ–≤–∞—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞
        $this->command->info('‚úÖ –í—Å–µ–≥–æ —Å–æ–∑–¥–∞–Ω–æ —Ç–æ–≤–∞—Ä–æ–≤: ' . $totalProducts);
        $this->command->info('üì∏ –í—Å–µ–≥–æ —Å–æ–∑–¥–∞–Ω–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π: ' . ProductImage::count());
        $this->command->info('üè∑Ô∏è  –¢–æ–≤–∞—Ä—ã –ø—Ä–∏–≤—è–∑–∞–Ω—ã –∫ —Ç–µ–≥–∞–º –∏ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º');
    }

    /**
     * –°–æ–∑–¥–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–æ–≤ –∫–æ—Ñ–µ (30-35 —à—Ç)
     * 
     * –†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º:
     * - –ö–æ—Ñ–µ –≤ –∑–µ—Ä–Ω–∞—Ö -> –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–∏ (–ø–æ 4-5 —Ç–æ–≤–∞—Ä–æ–≤ –Ω–∞ –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏—é)
     * - –ú–æ–ª–æ—Ç—ã–π –∫–æ—Ñ–µ (–Ω–µ—Å–∫–æ–ª—å–∫–æ —Ç–æ–≤–∞—Ä–æ–≤)
     * 
     * @return int –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–∑–¥–∞–Ω–Ω—ã—Ö —Ç–æ–≤–∞—Ä–æ–≤
     */
    private function createCoffeeProducts(): int
    {
        $count = 0;

        // –ü–æ–ª—É—á–∞–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é "–ö–æ—Ñ–µ –≤ –∑–µ—Ä–Ω–∞—Ö"
        $coffeeBeansCat = $this->categories->get('kofe-v-zernah');
        
        // –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∫–æ—Ñ–µ (–ö–æ–ª—É–º–±–∏—è, –≠—Ñ–∏–æ–ø–∏—è, –ö–µ–Ω–∏—è –∏ —Ç.–¥.)
        $coffeeSubcategories = Category::where('parent_id', $coffeeBeansCat->id)->get();

        // –ù–∞–∑–≤–∞–Ω–∏—è –¥–ª—è –∫–æ—Ñ–µ –≤ –∑–µ—Ä–Ω–∞—Ö (–±—É–¥—É—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è –¥–ª—è –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–π)
        $coffeeBeanNames = [
            '–ö–æ–ª—É–º–±–∏—è' => ['Colombia Supremo', '–ö–æ–ª—É–º–±–∏—è –£–∏–ª–∞', '–ö–æ–ª—É–º–±–∏—è –≠–∫—Å–µ–ª—å—Å–æ', '–ö–æ–ª—É–º–±–∏—è SL28'],
            '–≠—Ñ–∏–æ–ø–∏—è' => ['–≠—Ñ–∏–æ–ø–∏—è –ò—Ä–≥–∞—á–∏—Ñ', '–≠—Ñ–∏–æ–ø–∏—è –°–∏–¥–∞–º–æ', '–≠—Ñ–∏–æ–ø–∏—è –•–∞—Ä–∞—Ä', '–≠—Ñ–∏–æ–ø–∏—è –ô–æ—Ä–≥–∞—á–µ—Ñ—Ñ–µ'],
            '–ö–µ–Ω–∏—è' => ['–ö–µ–Ω–∏—è –ê–ê', '–ö–µ–Ω–∏—è AB', '–ö–µ–Ω–∏—è –ü–∏–±–µ—Ä—Ä–∏', '–ö–µ–Ω–∏—è –ö–∏–∞–º–±—É'],
            '–ë—Ä–∞–∑–∏–ª–∏—è' => ['–ë—Ä–∞–∑–∏–ª–∏—è –°–∞–Ω—Ç–æ—Å', '–ë—Ä–∞–∑–∏–ª–∏—è –ú–æ–¥–∂–∏–∞–Ω–∞', '–ë—Ä–∞–∑–∏–ª–∏—è –°–µ—Ä—Ä–∞–¥–æ', '–ë—Ä–∞–∑–∏–ª–∏—è –°–µ—Ä–∞–¥–æ –ú–∏–Ω–∞—Å'],
            '–ì–≤–∞—Ç–µ–º–∞–ª–∞' => ['–ì–≤–∞—Ç–µ–º–∞–ª–∞ –ê–Ω—Ç–∏–≥—É–∞', '–ì–≤–∞—Ç–µ–º–∞–ª–∞ –£—ç—É—ç—Ç–µ–Ω–∞–Ω–≥–æ', '–ì–≤–∞—Ç–µ–º–∞–ª–∞ –ê—Ç–∏—Ç–ª–∞–Ω'],
            '–ö–æ—Å—Ç–∞-–†–∏–∫–∞' => ['–ö–æ—Å—Ç–∞-–†–∏–∫–∞ –¢–∞—Ä—Ä–∞–∑—É', '–ö–æ—Å—Ç–∞-–†–∏–∫–∞ –¶–µ–Ω—Ç—Ä–∞–ª—å–Ω–∞—è –î–æ–ª–∏–Ω–∞', '–ö–æ—Å—Ç–∞-–†–∏–∫–∞ –ì–µ—Ä–∞–¥–∏—è'],
            '–≠—Å–ø—Ä–µ—Å—Å–æ-—Å–º–µ—Å–∏' => ['–≠—Å–ø—Ä–µ—Å—Å–æ –ö–ª–∞—Å—Å–∏–∫', '–≠—Å–ø—Ä–µ—Å—Å–æ –ö—Ä–µ–º–∞', '–≠—Å–ø—Ä–µ—Å—Å–æ –ò–Ω—Ç–µ–Ω—Å–æ', '–≠—Å–ø—Ä–µ—Å—Å–æ –î–µ–ª—é–∫—Å'],
            '–ò–Ω–¥–æ–Ω–µ–∑–∏—è' => ['–°—É–º–∞—Ç—Ä–∞ –ú–∞–Ω–¥–µ–ª–∏–Ω–≥', '–Ø–≤–∞ –ê—Ä–∞–±–∏–∫–∞', '–°—É–ª–∞–≤–µ—Å–∏ –¢–æ—Ä–∞—è', '–ë–∞–ª–∏ –ö–∏–Ω—Ç–∞–º–∞–Ω–∏'],
        ];

        // –°–æ–∑–¥–∞–µ–º –ø–æ 4 —Ç–æ–≤–∞—Ä–∞ –¥–ª—è –∫–∞–∂–¥–æ–π –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∫–æ—Ñ–µ
        foreach ($coffeeSubcategories as $subcategory) {
            $names = $coffeeBeanNames[$subcategory->name] ?? ['–ö–æ—Ñ–µ ' . $subcategory->name];
            
            foreach ($names as $productName) {
                $product = $this->createCoffeeProduct($productName, $subcategory->id);
                
                if ($product) {
                    $count++;
                    $this->command->line("   ‚úì {$productName} (–∫–∞—Ç–µ–≥–æ—Ä–∏—è: {$subcategory->name})");
                }
            }
        }

        // –°–æ–∑–¥–∞–µ–º –Ω–µ—Å–∫–æ–ª—å–∫–æ —Ç–æ–≤–∞—Ä–æ–≤ –¥–ª—è –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ "–ú–æ–ª–æ—Ç—ã–π –∫–æ—Ñ–µ" (3-5 —à—Ç)
        $groundCoffeeCat = $this->categories->get('molotyi-kofe');
        $groundCoffeeNames = [
            '–ú–æ–ª–æ—Ç—ã–π –∫–æ—Ñ–µ –ê—Ä–∞–±–∏–∫–∞ 100%',
            '–ú–æ–ª–æ—Ç—ã–π —ç—Å–ø—Ä–µ—Å—Å–æ-—Å–º–µ—Å—å',
            '–ú–æ–ª–æ—Ç—ã–π –¥–ª—è —Ç—É—Ä–∫–∏ –º–µ–ª–∫–∏–π –ø–æ–º–æ–ª',
            '–ú–æ–ª–æ—Ç—ã–π —É–Ω–∏–≤–µ—Ä—Å–∞–ª—å–Ω—ã–π —Å—Ä–µ–¥–Ω–∏–π –ø–æ–º–æ–ª',
        ];

        foreach ($groundCoffeeNames as $productName) {
            $product = $this->createCoffeeProduct($productName, $groundCoffeeCat->id);
            
            if ($product) {
                $count++;
                $this->command->line("   ‚úì {$productName}");
            }
        }

        return $count;
    }

    /**
     * –°–æ–∑–¥–∞—Ç—å –æ–¥–∏–Ω —Ç–æ–≤–∞—Ä –∫–æ—Ñ–µ
     * 
     * @param string $name –ù–∞–∑–≤–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–∞
     * @param int $categoryId ID –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
     * @return Product|null –°–æ–∑–¥–∞–Ω–Ω—ã–π —Ç–æ–≤–∞—Ä –∏–ª–∏ null –ø—Ä–∏ –æ—à–∏–±–∫–µ
     */
    private function createCoffeeProduct(string $name, int $categoryId): ?Product
    {
        try {
            // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —É–Ω–∏–∫–∞–ª—å–Ω—ã–π slug
            $slug = Str::slug($name) . '-' . fake()->numberBetween(1, 999);

            // –°–æ–∑–¥–∞–µ–º —Ç–æ–≤–∞—Ä –∫–æ—Ñ–µ
            $product = Product::create([
                'category_id' => $categoryId,
                'name' => $name,
                'slug' => $slug,
                'description' => $this->generateCoffeeDescription($name),
                'long_description' => $this->generateCoffeeLongDescription($name),
                'price' => fake()->randomFloat(2, 300, 800),
                'old_price' => fake()->boolean(30) ? fake()->randomFloat(2, 850, 1200) : null,
                'weight' => fake()->randomElement([250, 500, 1000]),
                'sku' => 'CF-' . fake()->unique()->numberBetween(1000, 9999),
                'stock' => fake()->numberBetween(5, 100),
                'rating' => fake()->randomFloat(2, 4.0, 5.0),
                'reviews_count' => 0, // –ë—É–¥–µ—Ç –æ–±–Ω–æ–≤–ª–µ–Ω–æ –ø–æ—Å–ª–µ —Å–æ–∑–¥–∞–Ω–∏—è –æ—Ç–∑—ã–≤–æ–≤
                'bitterness_percent' => fake()->randomElement([0, 2, 4, 6, 8, 10]),
                'acidity_percent' => fake()->randomElement([0, 2, 4, 6, 8, 10]),
                'is_featured' => fake()->boolean(25), // 25% —Ç–æ–≤–∞—Ä–æ–≤ —Ä–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–µ
                'is_available' => true,
                'meta_title' => $name . ' - –∫—É–ø–∏—Ç—å –∫–æ—Ñ–µ –≤ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç-–º–∞–≥–∞–∑–∏–Ω–µ',
                'meta_description' => "–ö—É–ø–∏—Ç—å {$name} –ø–æ –≤—ã–≥–æ–¥–Ω–æ–π —Ü–µ–Ω–µ. –°–≤–µ–∂–∞—è –æ–±–∂–∞—Ä–∫–∞, –¥–æ—Å—Ç–∞–≤–∫–∞ –ø–æ –†–æ—Å—Å–∏–∏.",
            ]);

            // –î–æ–±–∞–≤–ª—è–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∫ —Ç–æ–≤–∞—Ä—É (2-4 –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è)
            $this->addProductImages($product, 'coffee');

            // –ü—Ä–∏–≤—è–∑—ã–≤–∞–µ–º —Ç–µ–≥–∏ –∫ —Ç–æ–≤–∞—Ä—É (1-3 —Ç–µ–≥–∞)
            $this->attachRandomTags($product);

            return $product;
            
        } catch (\Exception $e) {
            $this->command->error("   ‚úó –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ç–æ–≤–∞—Ä–∞ {$name}: " . $e->getMessage());
            return null;
        }
    }

    /**
     * –°–æ–∑–¥–∞–Ω–∏–µ —Ç–æ–≤–∞—Ä–æ–≤ —á–∞—è (8-10 —à—Ç)
     * 
     * –†–∞–∑–ª–∏—á–Ω—ã–µ –≤–∏–¥—ã —á–∞—è: –∑–µ–ª–µ–Ω—ã–π, —á–µ—Ä–Ω—ã–π, —É–ª—É–Ω, –±–µ–ª—ã–π, –ø—É—ç—Ä
     * 
     * @return int –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–∑–¥–∞–Ω–Ω—ã—Ö —Ç–æ–≤–∞—Ä–æ–≤
     */
    private function createTeaProducts(): int
    {
        $count = 0;

        // –ü–æ–ª—É—á–∞–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é "–ß–∞–π"
        $teaCat = $this->categories->get('chay');

        if (!$teaCat) {
            $this->command->error('   ‚úó –ö–∞—Ç–µ–≥–æ—Ä–∏—è "–ß–∞–π" –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!');
            return 0;
        }

        // –°–ø–∏—Å–æ–∫ —Ç–æ–≤–∞—Ä–æ–≤ —á–∞—è
        $teaNames = [
            '–ó–µ–ª–µ–Ω—ã–π —á–∞–π –°–µ–Ω—á–∞',
            '–ß–µ—Ä–Ω—ã–π —á–∞–π –≠—Ä–ª –ì—Ä–µ–π',
            '–£–ª—É–Ω –¢–µ –ì—É–∞–Ω—å –ò–Ω—å',
            '–ë–µ–ª—ã–π —á–∞–π –ë–∞–π –•–∞–æ –ò–Ω—å –ß–∂–µ–Ω—å',
            '–ü—É—ç—Ä –®—É 5 –ª–µ—Ç –≤—ã–¥–µ—Ä–∂–∫–∏',
            '–ñ–∞—Å–º–∏–Ω–æ–≤—ã–π —á–∞–π –ú–∞–æ –§—ç–Ω',
            '–ú–∞—Ç—á–∞ –ø—Ä–µ–º–∏—É–º –∫–ª–∞—Å—Å',
            '–†–æ–π–±—É—à –≤–∞–Ω–∏–ª—å–Ω—ã–π',
            '–ß–µ—Ä–Ω—ã–π —á–∞–π –ê—Å—Å–∞–º',
            '–î–∞—Ä–¥–∂–∏–ª–∏–Ω–≥ –ø–µ—Ä–≤—ã–π —Å–±–æ—Ä',
        ];

        foreach ($teaNames as $productName) {
            $product = $this->createTeaProduct($productName, $teaCat->id);
            
            if ($product) {
                $count++;
                $this->command->line("   ‚úì {$productName}");
            }
        }

        return $count;
